<h2>Polityka Cookies serwisu dev.salon-bw.pl</h2>
<p>Serwis dev.salon-bw.pl korzysta z plików cookies i podobnych technologii, by zapewnić poprawne funkcjonowanie strony, analizować ruch oraz personalizować treści.</p>
<ol>
<li><strong>Czym są pliki cookies?</strong><br>Pliki cookies to niewielkie pliki tekstowe zapisywane na urządzeniu końcowym użytkownika podczas korzystania z serwisu.</li>
<li><strong>Cele i rodzaje cookies:</strong>
  <ul>
    <li><strong>Niezbędne:</strong> Umożliwiają korzystanie z podstawowych funkcji serwisu.</li>
    <li><strong>Analityczne:</strong> Umożliwiają zbieranie danych statystycznych o sposobie korzystania z serwisu (np. Google Analytics).</li>
    <li><strong>Marketingowe:</strong> Służą do wyświetlania spersonalizowanych reklam.</li>
  </ul>
</li>
<li><strong>Cookies podmiotów trzecich</strong><br>Korzystamy z usług analitycznych i marketingowych dostawców zewnętrznych, np. Google Analytics, Facebook Pixel.</li>
<li><strong>Zarządzanie cookies</strong>
  <ul>
    <li>Przy pierwszej wizycie wyświetlamy baner z możliwością wyboru preferowanych rodzajów cookies.</li>
    <li>Użytkownik może w dowolnym momencie zmienić ustawienia cookies w [panelu zarządzania cookies] lub w ustawieniach przeglądarki.</li>
  </ul>
</li>
<li><strong>Jak zablokować cookies?</strong><br>Użytkownik może w każdej chwili usunąć lub zablokować pliki cookies za pomocą ustawień swojej przeglądarki, jednak może to wpłynąć na funkcjonalność serwisu.</li>
<li><strong>Więcej informacji</strong><br>Szczegółowe informacje na temat plików cookies znajdziesz w [polityce prywatności] oraz w panelu zarządzania zgodami.</li>
</ol>
