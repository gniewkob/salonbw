<h2>Reklamacje i odstąpienie od umowy</h2>
<p>
    <strong>Reklamacje:</strong><br>
    Wszelkie reklamacje dotyczące usług świadczonych za pośrednictwem serwisu należy zgłaszać na adres e-mail: [adres e-mail] lub poprzez formularz kontaktowy dostępny na stronie.
    Reklamacje rozpatrujemy nie później niż w ciągu 14 dni od dnia ich otrzymania.
</p>
<p>
    <strong>Prawo odstąpienia od umowy:</strong><br>
    Zgodnie z ustawą o prawach konsumenta, klient ma prawo odstąpić od umowy zawartej na odległość w terminie 14 dni od jej zawarcia, z wyjątkiem sytuacji określonych w art. 38, m.in. w przypadku usług świadczonych w oznaczonym terminie (np. rezerwacja wizyty w salonie na konkretny dzień i godzinę).
</p>
<p>
    <strong>Anulowanie rezerwacji:</strong><br>
    W przypadku chęci anulowania rezerwacji, prosimy o kontakt z obsługą serwisu nie później niż [np. 24h] przed planowaną wizytą. Zasady anulowania i ewentualnego zwrotu opłaty są opisane w [regulaminie serwisu].
</p>
